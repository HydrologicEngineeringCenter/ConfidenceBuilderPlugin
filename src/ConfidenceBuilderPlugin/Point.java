package ConfidenceBuilderPlugin;

public class Point {
    //fields
    private final double _x;
    private final double _y;
    //getters
    public double getX() {
        return _x;
    }
    public double getY() {
        return _y;
    }
    //constructor
    public Point(double x, double y){
        _x = x;
        _y = y;
    }
}

